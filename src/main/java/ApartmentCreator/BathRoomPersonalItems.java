package ApartmentCreator;

public class BathRoomPersonalItems {

    public String towels;

    public String getTowels() {
        return towels;
    }

    public void setTowels(String towels) {
        this.towels = towels;
    }

    @Override
    public String toString() {
        return "BathRoomPersonalItems{" +
                "towels='" + towels + '\'' +
                '}';
    }
}
